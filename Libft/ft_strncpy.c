char		*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int b;

	b = 0;
	while (src[b] && b <= n)
	{
		dest[b] = src[b];
		b++;
	}
	dest[b + 1] = '\0';
	return (dest);
}
