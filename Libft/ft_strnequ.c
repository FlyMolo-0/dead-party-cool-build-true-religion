int		ft_strequ(char const *s1, char const *s2, size_t n)
{
	int a;

	a = 0;
	while (s1[a] == s2[a] != 0 && a < n)
	{
		a++;
		if (s1[a] == 0 || a == n)
			return (1);
	}
	return (0);
}
