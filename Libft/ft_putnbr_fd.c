#include <unistd.h>

void        ft_putnbr_fd(int nb)
{
	if (nb < 0)
	{
		ft_putchar_fd('-');
	}
	if (nb >= 10)
	{
		ft_putnbr_fd(nb / 10);
		ft_putnbr_fd(nb % 10);
	}
	else
	{
		ft_putchar_fd(nb + '0');
	}
	return;
}
