#include <unistd.h>

int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int a;

	a = 0;
	while (s1[a] == s2[a])
	{
		a++;
		if (s1[a] == 0)
			return (0);
		if (a == n)
			return (0);
	}
	return (s1[a] - s2[a]);
}
